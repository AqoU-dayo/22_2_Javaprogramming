import java.util.Scanner;

public class java_star {

	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		System.out.print("원하는 별의 층수를 입력하세요 : ");
		int floor = scanner.nextInt();
		
		for(int i=1; i<=floor; i++)
		{
			for(int j=1; j<=i; j++)
			{
				System.out.print("*");
			}
			System.out.println("");
		}
		
		scanner.close();
	}

}
